// Declare a variable and assign it a string value representing a day of the week
let day = "Wednesday";

// Use a switch statement to perform actions based on the day
switch (day) {
  case "Monday":
    console.log("Monday: Start of the week.");
    break;
  case "Tuesday":
    console.log("Tuesday: Keep the momentum going.");
    break;
  case "Wednesday":
    console.log("Wednesday: Midweek check-in.");
    break;
  case "Thursday":
    console.log("Thursday: Almost there!");
    break;
  case "Friday":
    console.log("Friday: Wrap up the week.");
    break;
  case "Saturday":
    console.log("Saturday: Time to relax.");
    break;
  case "Sunday":
    console.log("Sunday: Prepare for the new week.");
    break;
  default:
    console.log("Invalid day.");
}